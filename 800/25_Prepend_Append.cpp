void solve() {
    int n;
    cin>>n;
    string s;
    cin>>s;
    int l=0;
    int r=n-1;
    while(l<r){ // at all point of time I want the pointer to point at distinct elements i.e. they should not point to the same element 
        if(s[l]!=s[r]){
            n-=2;
            --r;
            ++l;
        }
        else{
            break;
        }
    }
    cout<<n<<endl;




    
}



